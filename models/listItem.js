export class listItem {
    constructor(
        Id,
        title,
        description,
        imageLink,
        externalLink,
        recType
    ) {
        this.Id = Id
        this.title = title,
        this.description = description,
        this.imageLink = imageLink,
        this.externalLink = externalLink,
        this.recType = recType
    }
}

export default listItem;